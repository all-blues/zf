# 星星
def star(color,x,y):
    penup()
    goto(x,y)
    pencolor(color)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
    penup()
    goto(x,y)
    right(45)
    pendown()
    forward(10)
# 随机生成星星
speed(1000)
for i in range(15):
    x=random.randint(-330,330)
    y=random.randint(180,280)
    color=random.choice(['deep pink','blue','brown','red'])
    star(color,x,y)
